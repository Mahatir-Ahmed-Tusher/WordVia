/**
 * Get word definition - prioritizes COMMON_WORD_DEFINITIONS, then falls back to API
 * This ensures we use local lexical resources first for reliability and speed
 */
export async function getWordDefinition(word: string): Promise<string | null> {
  const lowerWord = word.toLowerCase().trim();
  
  // First priority: Check COMMON_WORD_DEFINITIONS (local lexical resource)
  if (COMMON_WORD_DEFINITIONS[lowerWord]) {
    return COMMON_WORD_DEFINITIONS[lowerWord];
  }
  
  // Second priority: Check if word is in COMMON_VALID_WORDS (should have definition)
  // This is a safety check - if word is in COMMON_VALID_WORDS but definition missing, return null
  if (COMMON_VALID_WORDS.has(lowerWord)) {
    // Word is valid but definition might be missing, return null to try API
    return null;
  }
  
  // Third priority: Check cache
  if (wordCache.has(lowerWord)) {
    const cached = wordCache.get(lowerWord)!;
    if (cached.definition) {
      return cached.definition;
    }
  }
  
  // Fourth priority: Fetch from API
  try {
    const response = await fetch(`${API_BASE}/${encodeURIComponent(lowerWord)}`);
    
    if (!response.ok) {
      return null;
    }
    
    const data: DictionaryApiResponse[] = await response.json();
    
    if (data && data.length > 0 && data[0].meanings && data[0].meanings.length > 0) {
      const firstDef = data[0].meanings[0].definitions[0]?.definition || '';
      if (firstDef) {
        // Cache the definition for future use
        const cached = wordCache.get(lowerWord);
        if (cached) {
          cached.definition = firstDef;
        } else {
          wordCache.set(lowerWord, { isValid: true, definition: firstDef });
        }
        return firstDef;
      }
    }
  } catch (error) {
    console.error('Error fetching definition from API:', error);
  }
  
  return null;
}

/**
 * Validate a word using the Dictionary API
 * Only accepts proper words, not abbreviations
 * 
 * Priority order (ensures COMMON_VALID_WORDS is checked first):
 * 1. COMMON_VALID_WORDS (local lexical resource) - fastest and most reliable
 * 2. Cache (previously validated words)
 * 3. Dictionary API (fallback for words not in COMMON_VALID_WORDS)
 */
export async function validateWord(word: string): Promise<{ isValid: boolean; definition?: string; restriction?: WordRestriction }> {
  const lowerWord = word.toLowerCase().trim();
  
  // Reject words that are too short
  if (lowerWord.length < MIN_WORD_LENGTH) {
    const result = { isValid: false };
    wordCache.set(lowerWord, result);
    return result;
  }

  // For 2-letter words, only accept if in our valid list
  if (lowerWord.length === 2) {
    if (VALID_TWO_LETTER_WORDS.has(lowerWord)) {
      const definition = TWO_LETTER_DEFINITIONS[lowerWord] || 'Valid 2-letter word';
      const result = { isValid: true, definition };
      wordCache.set(lowerWord, result);
      return result;
    } else {
      const result = { isValid: false };
      wordCache.set(lowerWord, result);
      return result;
    }
  }

  // Check common words list first (bypass API for reliability)
  if (COMMON_VALID_WORDS.has(lowerWord)) {
    const definition = COMMON_WORD_DEFINITIONS[lowerWord] || 'Common English word';
    const result = { isValid: true, definition };
    wordCache.set(lowerWord, result);
    return result;
  }

  // Check cache first
  if (wordCache.has(lowerWord)) {
    return wordCache.get(lowerWord)!;
  }

  try {
    const response = await fetch(`${API_BASE}/${encodeURIComponent(lowerWord)}`);
    
    if (!response.ok) {
      // Fallback to COMMON_VALID_WORDS if API returns error
      if (COMMON_VALID_WORDS.has(lowerWord)) {
        const definition = COMMON_WORD_DEFINITIONS[lowerWord] || 'Common English word';
        const result = { isValid: true, definition };
        wordCache.set(lowerWord, result);
        return result;
      }
      const result = { isValid: false };
      wordCache.set(lowerWord, result);
      return result;
    }

    const data: DictionaryApiResponse[] = await response.json();
    
    if (data && data.length > 0) {
      // Check if the returned word matches our query (not a redirect to another word)
      const returnedWord = data[0].word?.toLowerCase();
      
      // If the API redirected to a different word, it might be an abbreviation
      if (returnedWord && returnedWord !== lowerWord) {
        const result = { isValid: false };
        wordCache.set(lowerWord, result);
        return result;
      }

      // Check if it has actual meanings (not just phonetics)
      if (data[0].meanings && data[0].meanings.length > 0) {
        const firstMeaning = data[0].meanings[0];
        
        // Check if it's marked as an abbreviation in the definition
        const firstDef = firstMeaning.definitions[0]?.definition || '';
        const isAbbreviation = 
          firstDef.toLowerCase().includes('abbreviation') ||
          firstDef.toLowerCase().includes('short for') ||
          firstDef.toLowerCase().includes('informal for') ||
          (firstMeaning.partOfSpeech === 'noun' && firstDef.includes('format')) ||
          (firstMeaning.partOfSpeech === 'noun' && /^\([^)]*\)/.test(firstDef));

        if (isAbbreviation) {
          const result = { isValid: false };
          wordCache.set(lowerWord, result);
          return result;
        }

        // Check for word restrictions (past tense, plural, etc.)
        const restriction = checkWordRestriction(lowerWord, data[0]);
        
        const result = { 
          isValid: true, 
          definition: firstDef,
          restriction,
        };
        wordCache.set(lowerWord, result);
        return result;
      }
    }

    const result = { isValid: false };
    wordCache.set(lowerWord, result);
    return result;
  } catch (error) {
    console.error('Word validation error:', error);
    // Fallback to COMMON_VALID_WORDS if API fails
    if (COMMON_VALID_WORDS.has(lowerWord)) {
      const definition = COMMON_WORD_DEFINITIONS[lowerWord] || 'Common English word';
      const result = { isValid: true, definition };
      wordCache.set(lowerWord, result);
      return result;
    }
    const result = { isValid: false };
    wordCache.set(lowerWord, result);
    return result;
  }
}

/**
 * Extract all words from the grid that pass through the newly placed letter
 * Only checks horizontal (leftright) and vertical (topbottom) directions
 */
export function extractWordsFromGrid(
  grid: Cell[][],
  placedRow: number,
  placedCol: number
): { word: string; cells: { row: number; col: number }[] }[] {
  const words: { word: string; cells: { row: number; col: number }[] }[] = [];
  const gridSize = grid.length;

  // Extract horizontal word (left to right)
  let startCol = placedCol;
  while (startCol > 0 && grid[placedRow][startCol - 1].letter !== '') {
    startCol--;
  }
  
  let horizontalWord = '';
  const horizontalCells: { row: number; col: number }[] = [];
  
  for (let col = startCol; col < gridSize && grid[placedRow][col].letter !== ''; col++) {
    horizontalWord += grid[placedRow][col].letter;
    horizontalCells.push({ row: placedRow, col });
  }
  
  // Only add if word is long enough
  if (horizontalWord.length >= MIN_WORD_LENGTH) {
    words.push({ word: horizontalWord, cells: horizontalCells });
  }

  // Extract vertical word (top to bottom)
  let startRow = placedRow;
  while (startRow > 0 && grid[startRow - 1][placedCol].letter !== '') {
    startRow--;
  }
  
  let verticalWord = '';
  const verticalCells: { row: number; col: number }[] = [];
  
  for (let row = startRow; row < gridSize && grid[row][placedCol].letter !== ''; row++) {
    verticalWord += grid[row][placedCol].letter;
    verticalCells.push({ row, col: placedCol });
  }
  
  // Only add if word is long enough
  if (verticalWord.length >= MIN_WORD_LENGTH) {
    words.push({ word: verticalWord, cells: verticalCells });
  }

  return words;
}

/**
 * Extract all possible subwords that contain the placed letter
 * For disambiguation when the full word is invalid
 */
export function extractSubwordsContainingLetter(
  grid: Cell[][],
  placedRow: number,
  placedCol: number,
  direction: 'horizontal' | 'vertical'
): string[] {
  const gridSize = grid.length;
  const subwords: string[] = [];

  if (direction === 'horizontal') {
    // Find the full horizontal sequence
    let startCol = placedCol;
    while (startCol > 0 && grid[placedRow][startCol - 1].letter !== '') {
      startCol--;
    }
    
    let endCol = placedCol;
    while (endCol < gridSize - 1 && grid[placedRow][endCol + 1].letter !== '') {
      endCol++;
    }
    
    // Build the full word
    let fullWord = '';
    for (let col = startCol; col <= endCol; col++) {
      fullWord += grid[placedRow][col].letter;
    }
    
    // Position of placed letter in the full word
    const placedIndex = placedCol - startCol;
    
    // Generate all substrings that contain the placed letter
    for (let start = 0; start <= placedIndex; start++) {
      for (let end = placedIndex; end < fullWord.length; end++) {
        const subword = fullWord.substring(start, end + 1);
        if (subword.length >= MIN_WORD_LENGTH && !subwords.includes(subword)) {
          subwords.push(subword);
        }
      }
    }
  } else {
    // Vertical direction
    let startRow = placedRow;
    while (startRow > 0 && grid[startRow - 1][placedCol].letter !== '') {
      startRow--;
    }
    
    let endRow = placedRow;
    while (endRow < gridSize - 1 && grid[endRow + 1][placedCol].letter !== '') {
      endRow++;
    }
    
    // Build the full word
    let fullWord = '';
    for (let row = startRow; row <= endRow; row++) {
      fullWord += grid[row][placedCol].letter;
    }
    
    // Position of placed letter in the full word
    const placedIndex = placedRow - startRow;
    
    // Generate all substrings that contain the placed letter
    for (let start = 0; start <= placedIndex; start++) {
      for (let end = placedIndex; end < fullWord.length; end++) {
        const subword = fullWord.substring(start, end + 1);
        if (subword.length >= MIN_WORD_LENGTH && !subwords.includes(subword)) {
          subwords.push(subword);
        }
      }
    }
  }

  // Sort by length descending (longest first), but ensure at least 2 smallest are shown
  subwords.sort((a, b) => b.length - a.length);
  
  return subwords;
}

/**
 * Validate multiple subwords and return their validity status
 */
export async function validateSubwords(
  subwords: string[]
): Promise<{ word: string; isValid: boolean; definition?: string }[]> {
  const results = await Promise.all(
    subwords.map(async (word) => {
      const validation = await validateWord(word);
      return {
        word,
        isValid: validation.isValid,
        definition: validation.definition,
      };
    })
  );
  return results;
}

/**
 * Check if disambiguation is needed for a direction
 * Returns subwords if the full word is invalid but subwords might be valid
 */
export async function checkDisambiguation(
  grid: Cell[][],
  placedRow: number,
  placedCol: number,
  direction: 'horizontal' | 'vertical'
): Promise<{ needsDisambiguation: boolean; subwords: { word: string; isValid: boolean; definition?: string }[] }> {
  const subwords = extractSubwordsContainingLetter(grid, placedRow, placedCol, direction);
  
  if (subwords.length <= 1) {
    return { needsDisambiguation: false, subwords: [] };
  }
  
  // Validate the longest word first (the full sequence)
  const fullWord = subwords[0];
  const fullWordValidation = await validateWord(fullWord);
  
  // If the full word is valid, no disambiguation needed
  if (fullWordValidation.isValid) {
    return { needsDisambiguation: false, subwords: [] };
  }
  
  // Full word is invalid, validate all subwords
  const validatedSubwords = await validateSubwords(subwords);
  
  // Check if any subword is valid
  const hasValidSubword = validatedSubwords.some(s => s.isValid);
  
  if (!hasValidSubword) {
    return { needsDisambiguation: false, subwords: validatedSubwords };
  }
  
  return { needsDisambiguation: true, subwords: validatedSubwords };
}

/**
 * Validate all words formed by the latest move
 */
export async function validateMove(
  grid: Cell[][],
  placedRow: number,
  placedCol: number
): Promise<WordResult[]> {
  const potentialWords = extractWordsFromGrid(grid, placedRow, placedCol);
  
  // If no potential words (all too short), return empty
  if (potentialWords.length === 0) {
    return [];
  }
  
  const results: WordResult[] = await Promise.all(
    potentialWords.map(async ({ word, cells }) => {
      const validation = await validateWord(word);
      return {
        word,
        isValid: validation.isValid,
        points: validation.isValid ? word.length : 0,
        definition: validation.definition,
        cells,
      };
    })
  );

  return results;
}

/**
 * Enhanced validation with disambiguation support
 * Returns either direct results or disambiguation options
 */
export async function validateMoveWithDisambiguation(
  grid: Cell[][],
  placedRow: number,
  placedCol: number
): Promise<{
  results: WordResult[];
  disambiguation?: {
    direction: 'horizontal' | 'vertical';
    options: { word: string; isValid: boolean; definition?: string }[];
  };
}> {
  const gridSize = grid.length;

  // Check horizontal sequence
  let hStartCol = placedCol;
  while (hStartCol > 0 && grid[placedRow][hStartCol - 1].letter !== '') {
    hStartCol--;
  }
  let hEndCol = placedCol;
  while (hEndCol < gridSize - 1 && grid[placedRow][hEndCol + 1].letter !== '') {
    hEndCol++;
  }
  const horizontalLength = hEndCol - hStartCol + 1;

  // Check vertical sequence
  let vStartRow = placedRow;
  while (vStartRow > 0 && grid[vStartRow - 1][placedCol].letter !== '') {
    vStartRow--;
  }
  let vEndRow = placedRow;
  while (vEndRow < gridSize - 1 && grid[vEndRow + 1][placedCol].letter !== '') {
    vEndRow++;
  }
  const verticalLength = vEndRow - vStartRow + 1;

  // Check horizontal disambiguation first if there's a horizontal word
  if (horizontalLength >= MIN_WORD_LENGTH) {
    const hDisambiguation = await checkDisambiguation(grid, placedRow, placedCol, 'horizontal');
    if (hDisambiguation.needsDisambiguation) {
      return {
        results: [],
        disambiguation: {
          direction: 'horizontal',
          options: hDisambiguation.subwords,
        },
      };
    }
  }

  // Check vertical disambiguation if there's a vertical word
  if (verticalLength >= MIN_WORD_LENGTH) {
    const vDisambiguation = await checkDisambiguation(grid, placedRow, placedCol, 'vertical');
    if (vDisambiguation.needsDisambiguation) {
      return {
        results: [],
        disambiguation: {
          direction: 'vertical',
          options: vDisambiguation.subwords,
        },
      };
    }
  }

  // No disambiguation needed, do normal validation
  const results = await validateMove(grid, placedRow, placedCol);
  return { results };
}
