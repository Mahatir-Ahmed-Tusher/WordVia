export type WordRestriction = 'past_tense' | 'past_participle' | 'plural' | 'proper_noun' | null;

/**
 * Check if a word appears to be a restricted form
 * Returns the restriction type or null if allowed
 */
export function checkWordRestriction(word: string, apiData?: any): WordRestriction {
  const lowerWord = word.toLowerCase().trim();
  
  // Check exceptions first
  if (EXCEPTIONS.has(lowerWord)) {
    return null;
  }
  
  // Check for proper noun (first letter capitalized in original, and API confirms it's a proper noun)
  if (apiData?.meanings) {
    const hasProperNoun = apiData.meanings.some((m: any) => 
      m.partOfSpeech?.toLowerCase() === 'proper noun' ||
      m.partOfSpeech?.toLowerCase() === 'noun' && m.definitions?.some((d: any) => 
        d.definition?.toLowerCase().includes('proper noun') ||
        d.definition?.toLowerCase().includes('a name') ||
        d.definition?.toLowerCase().includes('the name of')
      )
    );
    if (hasProperNoun) {
      return 'proper_noun';
    }
  }
  
  // Check for past participle patterns (irregular forms)
  const irregularPastParticiples = [
    'been', 'done', 'gone', 'seen', 'taken', 'given', 'eaten', 'fallen', 'chosen', 'frozen',
    'spoken', 'broken', 'stolen', 'woken', 'written', 'driven', 'ridden', 'risen', 'hidden',
    'bitten', 'forgotten', 'gotten', 'proven', 'shaken', 'mistaken', 'undertaken', 'striven',
    'sworn', 'worn', 'torn', 'born', 'borne', 'drawn', 'grown', 'known', 'shown', 'thrown', 'blown', 'flown',
    'begun', 'sung', 'rung', 'sprung', 'stung', 'swung', 'hung', 'flung', 'clung', 'slung', 'wrung',
    'drunk', 'shrunk', 'sunk', 'stunk',
    'brought', 'bought', 'caught', 'fought', 'sought', 'taught', 'thought', 'wrought',
    'bent', 'lent', 'sent', 'spent', 'built', 'burnt', 'dealt', 'dreamt', 'felt', 'kept', 'knelt',
    'leapt', 'learnt', 'left', 'lost', 'meant', 'met', 'slept', 'smelt', 'spelt', 'swept', 'wept',
    'bound', 'found', 'ground', 'wound', 'held', 'sold', 'told',
    'heard', 'paid', 'said', 'laid', 'made', 'had',
    'sat', 'set', 'put', 'cut', 'shut', 'let', 'hit', 'bit', 'quit', 'split', 'spit', 'rid', 'spread',
    'beat', 'run', 'come', 'become', 'overcome',
  ];
  
  // Remove common false positives from irregular list
  const excludeFromIrregular = new Set(['run', 'come', 'become', 'overcome', 'set', 'put', 'cut', 'shut', 'let', 'hit', 'quit', 'split', 'spit', 'rid', 'spread', 'beat']);
  
  if (irregularPastParticiples.includes(lowerWord) && !excludeFromIrregular.has(lowerWord)) {
    return 'past_participle';
  }
  
  // Check for regular past tense/participle ending in -ed
  if (lowerWord.endsWith('ed') && lowerWord.length > 3) {
    // Check if the base form exists (remove -ed, -d, or -ied)
    return 'past_tense';
  }
  
  // Check for plurals ending in -s (but not words naturally ending in -s like "bus", "grass")
  if (lowerWord.endsWith('s') && lowerWord.length > 3) {
    // Common plural patterns
    if (lowerWord.endsWith('ies') && lowerWord.length > 4) {
      return 'plural';
    }
    if (lowerWord.endsWith('es') && lowerWord.length > 4) {
      const stem = lowerWord.slice(0, -2);
      // Words ending in -ch, -sh, -s, -x, -z add -es
      if (stem.endsWith('ch') || stem.endsWith('sh') || stem.endsWith('x') || stem.endsWith('z')) {
        return 'plural';
      }
    }
    // General -s plural (but avoid false positives)
    if (!lowerWord.endsWith('ss') && !lowerWord.endsWith('us') && !lowerWord.endsWith('is')) {
      // Check if removing 's' gives a valid-looking base
      const potentialBase = lowerWord.slice(0, -1);
      if (potentialBase.length >= 3 && !potentialBase.endsWith('s')) {
        return 'plural';
      }
    }
  }
  
  return null;
}

